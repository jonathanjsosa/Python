# Addition
print(f"Addition \n2 + 1 = {2 + 1}")

# Subtraction
print(f"Subtraction \n2 - 1 = {2 - 1}")

# Multiplication
print(f"Multiplication \n2 * 2 = {2 * 2}")

# Division
print(f"Division \n3 / 2 = {3 / 2}")
print("\n")

''' Modulo or Mod Operator '''
# Normal Division
print(f"Division 7/4 = {7 / 4}")

# Mod Operator
print(f"Mod Operator (%) of 7/4 (7%4) = {7 % 4}")
print(f"Mod Operator (%) of 50/5 (50%5) = {50 % 5}")

# Power
print(f"Power \n2^3 = {2 ** 3}")
print("\n")

''' Order of Operations '''
print(f"2 + 10 * 10 + 3 = {2 + 10 * 10 + 3}")
print(f"(2 + 10) * (10 + 3) = {(2 + 10) * (10 + 3)}")

